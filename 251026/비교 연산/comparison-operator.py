a, b = input().split()

# 같거나 큰가
if a >= b:
    print(1)
else:
    print(0)

# 큰가
if a > b:
    print(1)
else:
    print(0)

# b가 a보다 같거나 큰가
if a <= b:
    print(1)
else:
    print(0)

# b가 a보다 큰가
if a < b:
    print(1)
else:
    print(0)

if a == b:
    print(1)
else:
    print(0)

if a != b:
    print(1)
else:
    print(0)
    